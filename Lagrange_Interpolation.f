	DIMENSION X(100),F(100)
	WRITE(*,*)'ENTER THE NUMBER OF TERMS; MAX: 100'
	READ(*,*) I
	WRITE(*,*)'ENTER THE VALUE OF X AND THE CORRESPONDING VALUE OF F(X)'
	DO 1 J=1,I
	READ(*,*),X(J),F(J)
1	CONTINUE
	WRITE(*,*)'THE ENTERED VALUES ARE'
	DO 2 J=1,I
	WRITE(*,3)X(J),F(J)
2	CONTINUE
3	FORMAT(F12.6'	'F12.6)
	A=X(1)
	B=X(1)
	DO 4 J=1,I
	IF(X(I).GT.A)THEN
	A=X(I)
	ENDIF
	IF(X(I).LT.B)THEN
	B=X(I)
	ENDIF
4	CONTINUE
	WRITE(*,*)'ENTER THE NUMBER AT WHICH THE VALUE OF FUNCTION IS REQUIRED'
5	READ(*,*),E
	IF((E.GT.A).OR.(E.LT.B))THEN
	WRITE(*,*)'INVALID INPUT FOR INTERPOLATION, ENTER AGAIN'
	GOTO 5
	ENDIF
	C=1.0
	D=1.0
	S=0.0
	DO 7 J=1,I
	D=1.0
	DO 6 K=1,I
	IF(J.NE.K) THEN
	C=(E-X(K))/(X(J)-X(K))
	D=D*C
	ENDIF
6	CONTINUE
	D=D*F(J)
	S=S+D
7	CONTINUE
	WRITE(*,*)'THE VALUE INTERPOLATED AT X =',E,' IS FOUND TO BE',S
	STOP
	END

