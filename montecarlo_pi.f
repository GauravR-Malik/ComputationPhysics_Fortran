C PROGRAM FOR MONTE CARLO DETERMINATION OF PI
C GAURAV RUDRA MALIK, R.NO:23
	DOUBLE PRECISION a,b
	F(L)=MOD(1956*L + 178592,67529)
	C=0
	D=0
	WRITE(*,*)'ENTER THE NUMBER OF HITS'
	READ(*,*)N
	WRITE(*,*)'ENTER THE SEED'
	READ(*,*)Q
	DO I=1,N
	Q=F(Q)
	P=F(Q)
	R=F(P)
	X=Q/67529
	Y=P/67529
	Z=R/67529
	IF((X**2+Y**2).LT.1)THEN
	C=C+1
	ENDIF
	IF((X**2+Y**2+Z**2).LT.1)THEN
	D=D+1
	ENDIF
	ENDDO
	  A=4*(C/N)
	  B=6*(D/N)
	WRITE(*,*)'THE VALUE OF PI IN 2D:',A
	WRITE(*,*)'THE VALUE OF PI IN 3D:',B
	STOP
	END

